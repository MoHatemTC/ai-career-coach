"""
app/api/v1/benchmarks.py
========================
FastAPI router that exposes:

  POST /analyze
    - Accepts a raw job description text.
    - Runs the LangGraph extraction + embedding pipeline.
    - Persists the result in PostgreSQL via SQLModel.
    - Returns the extracted benchmark data to the caller.

.. important::
   Benchmarks produced by this endpoint are **AI-generated suggestions**.
   They must be reviewed and validated by a human recruiter or hiring manager
   before being used in any hiring decision, job posting, or candidate
   evaluation.  The model may hallucinate, mis-classify, or omit information
   even when structured output validation succeeds.
"""

from __future__ import annotations

from datetime import datetime
import logging
from typing import Annotated, Optional

import openai
from fastapi import APIRouter, Body, Depends, HTTPException, status
from prometheus_client import Histogram
from pydantic import Field
from sqlmodel.ext.asyncio.session import AsyncSession

from app.db.connection import get_session
from app.schemas.role_benchmark import RoleBenchmark as RoleBenchmarkSchema
from app.services.role_benchmark_service import (
    mark_benchmark_reviewed,
    run_benchmark_pipeline,
    save_benchmark,
)

logger = logging.getLogger(__name__)

# ---------------------------------------------------------------------------
# Prometheus metrics
# ---------------------------------------------------------------------------

from app.core.metrics import get_or_create_metric

EXTRACTION_LATENCY = get_or_create_metric(
    Histogram,
    "benchmark_extraction_latency_seconds",
    "Time spent extracting benchmark data from LLM",
)

router = APIRouter(prefix="/benchmarks", tags=["benchmarks"])


# ---------------------------------------------------------------------------
# Request / response schemas (router-local; keeps API layer self-contained)
# ---------------------------------------------------------------------------





class AnalyzeResponse(RoleBenchmarkSchema):
    """
    The extracted benchmark data returned to the client.

    Inherits all fields from ``RoleBenchmarkSchema`` and adds:

    * ``id`` — database primary-key so the caller can retrieve or update the
      record later.
    * ``review_required`` — always ``True``; signals to API consumers that
      this benchmark is an **AI-generated suggestion** and must be reviewed
      by a human before use in any hiring decision.
    """

    id: int = Field(description="Database primary-key of the persisted benchmark.")
    review_required: bool = Field(
        default=True,
        description=(
            "Always true. Indicates that this benchmark was generated by an AI model "
            "and must be reviewed and validated by a human recruiter or hiring manager "
            "before being used in any hiring decision or candidate evaluation."
        ),
    )
    reviewed_at: Optional[datetime] = None

    model_config = {"from_attributes": True}


# ---------------------------------------------------------------------------
# Database session dependency
# ---------------------------------------------------------------------------
# ``get_session`` is imported from app.db.connection which owns the engine
# configuration and the connection-pool lifecycle.  This keeps the router
# free of any database wiring details.

SessionDep = Annotated[AsyncSession, Depends(get_session)]


# ---------------------------------------------------------------------------
# Endpoint
# ---------------------------------------------------------------------------


@router.post(
    "/analyze",
    response_model=AnalyzeResponse,
    status_code=status.HTTP_201_CREATED,
    summary="Extract a structured role benchmark from a job description (AI suggestion)",
    description=(
        "Paste the raw job description as plain text in the request body.\n\n"
        "**⚠ AI-Generated Suggestion — Human Review Required**\n\n"
        "The returned benchmark is produced by a language model and must be reviewed "
        "by a human recruiter before use in any hiring decision."
    ),
)
async def analyze_job_description(
    session: SessionDep,
    job_text: Annotated[
        str,
        Body(
            media_type="text/plain",
            min_length=50,
            description="Raw, unstructured job description (plain text).",
            examples=["We are looking for a Senior Backend Engineer with 5+ years of Python..."],
        ),
    ],
) -> AnalyzeResponse:
    """
    POST /benchmarks/analyze

    Extracts a structured role benchmark from the supplied job description
    using an LLM pipeline, persists the result to PostgreSQL, and returns it.

    .. important::
       The returned benchmark is an **AI-generated suggestion**.  A human
       recruiter or hiring manager must review it before it is used in any
       hiring decision or candidate evaluation.

    Parameters
    ----------
    body:
        ``{"job_text": "<raw job description>"}``
    session:
        Injected SQLModel database session.

    Returns
    -------
    AnalyzeResponse
        The extracted & persisted benchmark with its database ``id`` and
        ``review_required: true``.

    Raises
    ------
    422 Unprocessable Entity
        If the job text is too short (< 50 chars).
    502 Bad Gateway
        If the LLM fails to extract a valid benchmark after all retries.
    500 Internal Server Error
        For unexpected errors during the pipeline or persistence.
    """
    # ------------------------------------------------------------------
    # 1. Run the LangGraph pipeline (latency is observed via Prometheus)
    # ------------------------------------------------------------------
    try:
        with EXTRACTION_LATENCY.time():
            final_state = await run_benchmark_pipeline(raw_text=job_text)
    except ValueError as exc:
        logger.error("Extraction pipeline exhausted retries: %s", exc)
        raise HTTPException(
            status_code=status.HTTP_502_BAD_GATEWAY,
            detail=f"LLM extraction failed after maximum retries: {exc}",
        ) from exc
    except openai.RateLimitError as exc:
        logger.error("LLM rate limit hit: %s", exc)
        raise HTTPException(
            status_code=status.HTTP_503_SERVICE_UNAVAILABLE,
            detail="The AI model rate limit has been reached. Please retry shortly.",
        ) from exc
    except openai.AuthenticationError as exc:
        logger.error("LLM authentication failed: %s", exc)
        raise HTTPException(
            status_code=status.HTTP_502_BAD_GATEWAY,
            detail="AI model authentication failed. Check LITELLM_API_KEY.",
        ) from exc
    except openai.APIError as exc:
        logger.exception("LLM API error during extraction pipeline")
        raise HTTPException(
            status_code=status.HTTP_502_BAD_GATEWAY,
            detail=f"AI model returned an error: {exc}",
        ) from exc
    except Exception as exc:
        logger.exception("Unexpected error during extraction pipeline")
        raise HTTPException(
            status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
            detail="An unexpected error occurred during job description analysis.",
        ) from exc

    # ------------------------------------------------------------------
    # 2. Persist to PostgreSQL via the service layer
    # ------------------------------------------------------------------
    try:
        db_record = await save_benchmark(state=final_state, session=session)
    except RuntimeError as exc:
        raise HTTPException(
            status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
            detail=str(exc),
        ) from exc

    # ------------------------------------------------------------------
    # 3. Build and return the API response
    # ------------------------------------------------------------------
    extracted: RoleBenchmarkSchema = final_state["extracted_data"]

    return AnalyzeResponse(
        id=db_record.id,
        must_have_skills=extracted.must_have_skills,
        nice_to_have_skills=extracted.nice_to_have_skills,
        required_tools=extracted.required_tools,
        minimum_years=extracted.minimum_years,
        seniority_level=extracted.seniority_level,
        common_responsibilities=extracted.common_responsibilities,
        reviewed_at=db_record.reviewed_at,
        review_required=(db_record.reviewed_at is None),
    )


@router.patch("/{benchmark_id}/review", response_model=AnalyzeResponse, status_code=status.HTTP_200_OK)
async def review_benchmark(benchmark_id: int, session: AsyncSession = Depends(get_session)):
    """Record that a human has reviewed this benchmark."""
    try:
        benchmark = await mark_benchmark_reviewed(session, benchmark_id)
    except ValueError as ve:
        raise HTTPException(status_code=status.HTTP_404_NOT_FOUND, detail=str(ve))
    return AnalyzeResponse(
        id=benchmark.id,
        must_have_skills=benchmark.must_have_skills,
        nice_to_have_skills=benchmark.nice_to_have_skills,
        required_tools=benchmark.required_tools,
        minimum_years=benchmark.minimum_years,
        seniority_level=benchmark.seniority_level,
        common_responsibilities=benchmark.common_responsibilities,
        reviewed_at=benchmark.reviewed_at,
        review_required=(benchmark.reviewed_at is None),
    )
